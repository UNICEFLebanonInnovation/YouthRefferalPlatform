def get_choice_value(data, field_name, choice_name=None):
    if not data or field_name not in data:
        return ''
    value = data.get(field_name)
    if not value:
        return ''
    if not choice_name:
        choice_name = field_name
    try:
        choice_list = globals()[choice_name]
        return choice_list.get(value)
    except Exception as ex:
        # print(ex.message)
        return 'None'
